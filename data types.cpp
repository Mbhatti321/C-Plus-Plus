#include<iostream> // THIS IS LIBRARY
using namespace std; // THIS IS STANDARD
int main()  // THIS IS MAIN FUNCTION
{ // open brace
	int num=10; // int data type initialization
	float dig=10.98; // float data type initilization
	double hel=1873.2987;// double data type initilization
	char marks='c';// character data type initilization
	string start="Hello";// string data type initilization
	cout<<num<<"\n"; // calling num value
	cout<<dig<<"\n";// calling dig value
	cout<<hel<<"\n";// calling hel value
	cout<<marks<<"\n";// calling marks value
	cout<<start<<"\n";// calling start value
	return 0;
} // closing brace
